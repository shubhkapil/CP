
#include<bits/stdc++.h>
#define ll long long
using namespace std;

vector<int> oneD(int n)
{
	vector<int> v;
	int ele;
	for(int i = 0;i<n;i++)
	{
		cin>>ele;
		v.push_back(ele);
	}
	return v;
}

bool isSorted(vector<int> v)
{
	vector<int> sv = v;
	sort(sv.begin(),sv.end());
	return v==sv;
}

void solve()
{
	int n;
	cin>>n;
	vector<int> v = oneD(2*n);

	sort(v.begin(),v.end());

	int ans = 0;
	int i = 0;
	while(i<2*n)
	{
		ans+=v[i];
		i+=2;
	}

	cout<<ans<<endl;
}
